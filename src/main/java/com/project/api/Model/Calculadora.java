package com.project.api.Model;

public class Calculadora {

    public int resultSuma(int a, int b) {
        return a + b;
    }

    public int resultRest(int a, int b) {
        return a - b;
    }

    public int resultMultiply(int a, int b) {
        return a * b;
    }

    public int resultDivide(int a, int b) {
        return a / b;
    }
}
